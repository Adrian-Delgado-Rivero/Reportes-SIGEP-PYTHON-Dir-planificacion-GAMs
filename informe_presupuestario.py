def escribir_titulo_y_df(writer, hoja, titulo, df, startrow=0, hidden_cols=None):
    """Escribe un título encima de los encabezados del DataFrame.
    hidden_cols: lista de nombres de columnas a ocultar en Excel."""
    df.to_excel(writer, sheet_name=hoja, startrow=startrow+1, index=False)
    ws = writer.sheets[hoja]
    ws.write(startrow, 0, titulo)
    # Ajustar ancho de columnas básico
    for i, col in enumerate(df.columns, 0):
        max_len = max(df[col].astype(str).map(len).max(), len(str(col))) + 2
        ws.set_column(i, i, min(max_len, 40))

    # Ocultar columnas especificadas
    if hidden_cols:
        # Create a format object for hidden columns
        hidden_format = writer.book.add_format({'hidden': True})
        for col in hidden_cols:
            if col in df.columns:
                col_idx = df.columns.get_loc(col)
                ws.set_column(col_idx, col_idx, None, hidden_format) # Use the format object here

def calcular_percentiles_serie(serie):
    """
    Calcula los percentiles de referencia y asigna a cada valor su percentil.
    Retorna: (tabla_resumen, serie_percentiles_asignados)
    """
    # Percentiles de referencia
    puntos = [1, 10, 25, 50, 75, 90, 99]
    vals = {p: np.percentile(serie.dropna(), p) for p in puntos}

    # Asignar percentil a cada observación
    def asignar_percentil(x):
        if pd.isna(x):
            return np.nan
        # Si supera el percentil 99, se denomina 100
        if x > vals[99]:
            return 100
        # Buscar el percentil más alto que no supera el valor
        asignado = 1
        for p in puntos:
            if x >= vals[p]:
                asignado = p
        return asignado

    perc_asignado = serie.apply(asignar_percentil)

    # Tabla resumen
    tabla = pd.DataFrame({
        'P': puntos,
        'Valor': [vals[p] for p in puntos]
    })
    return tabla, perc_asignado

def reordenar_columnas(df, es_filtrada=False):
    """
    Reordena las columnas del DataFrame.
    - PRINCIPAL: columnas prioritarias al inicio, resto conserva orden original.
    - TABLA FILTRADA: igual, pero columnas de variables (Var_Multiple_*)
      se ubican obligatoriamente DESPUÉS de 'Diff_Vig_Prev'.
    """
    prioritarias = ['Entidad', 'TIPO', 'DA', 'UE', 'FTE', 'Org.', 'Cat. Prg.',
                    'Descripción Cat. Prg.', 'Objeto', 'Descripcion Objeto Del Gasto']
    prioritarias = [c for c in prioritarias if c in df.columns]

    if es_filtrada:
        vars_cols = ['Var_Multiple_FTE', 'FTE_2', 'FTE_3',
                     'Var_Multiple_Org', 'Org_2', 'Org_3', 'Concat_Org']
        vars_cols = [c for c in vars_cols if c in df.columns]

        # Resto: ni prioritarias, ni vars, ni Diff_Vig_Prev
        resto = [c for c in df.columns if c not in prioritarias + vars_cols + ['Diff_Vig_Prev']]

        # Ubicar Diff_Vig_Prev después de Preventivo (o al final si no existe)
        if 'Preventivo' in resto:
            idx = resto.index('Preventivo') + 1
            resto.insert(idx, 'Diff_Vig_Prev')
        elif 'Presup. Vig.' in resto:
            idx = resto.index('Presup. Vig.') + 1
            resto.insert(idx, 'Diff_Vig_Prev')
        else:
            resto.append('Diff_Vig_Prev')

        return df[prioritarias + resto + vars_cols]
    else:
        resto = [c for c in df.columns if c not in prioritarias]
        return df[prioritarias + resto]

