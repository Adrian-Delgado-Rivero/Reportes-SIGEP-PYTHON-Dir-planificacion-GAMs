def crear_resumenes(df_orig):
    resultados = {}

    # -- a.1 Agregado --
    for agr in ['Org', 'FTE', 'DA', 'UE']:
        if agr in df_orig.columns:
            res = df_orig.groupby(agr).agg({
                'Presup. Vig.': 'sum',
                'Preventivo': 'sum'
            }).reset_index()
            # a.1 no lleva Cat. Prg. ni Objeto, por tanto no aplica descripción
            resultados[f'a.1_Suma_by_{agr}'] = res

    # -- a.2 Desagregado por Cat. Prg. --
    if 'Cat. Prg.' in df_orig.columns and 'FTE' in df_orig.columns:
        res = df_orig.groupby(['Cat. Prg.', 'FTE']).agg({
            'Presup. Vig.': 'sum',
            'Preventivo': 'sum'
        }).reset_index()
        # Renombrar para que quede como Cat prog | FTE | Pres
        # INDICAR QUE 'Pres' SE TRATA DE Preventivo
        res.rename(columns={'Preventivo': 'Pres'}, inplace=True)
        # Añadir descripción de categoría programática
        if not desc_cat_map.empty:
            res['Descripción Cat. Prg.'] = res['Cat. Prg.'].map(desc_cat_map)
            res = res[['Cat. Prg.', 'Descripción Cat. Prg.', 'FTE', 'Pres']]
        else:
            res = res[['Cat. Prg.', 'FTE', 'Pres']]
        resultados['a.2_CatPrg_FTE'] = res

    # -- a.3 Desagregado por Objeto --
    for agr in ['Org', 'FTE', 'DA', 'UE']:
        if agr in df_orig.columns and 'Objeto' in df_orig.columns:
            res = df_orig.groupby(['Objeto', agr]).agg({
                'Mod. Aprobadas': 'sum'
            }).reset_index()
            # Añadir descripción del objeto
            if not desc_obj_map.empty:
                res['Descripcion Objeto Del Gasto'] = res['Objeto'].map(desc_obj_map)
                res = res[['Objeto', 'Descripcion Objeto Del Gasto', agr, 'Mod. Aprobadas']]
            resultados[f'a.3_Objeto_by_{agr}'] = res

    # -- a.4 Desagregado por Objeto > 0 --
    if 'Objeto' in df_orig.columns and 'Mod. Aprobadas' in df_orig.columns:
        df_pos = df_orig[df_orig['Mod. Aprobadas'] > 0].copy()
        if not df_pos.empty and 'FTE' in df_pos.columns:
            # Agregado por Objeto-FTE
            res = df_pos.groupby(['Objeto', 'FTE']).agg({
                'Mod. Aprobadas': 'sum'
            }).reset_index()
            # Calcular percentiles sobre la suma agregada
            tabla_perc, res['P'] = calcular_percentiles_serie(res['Mod. Aprobadas'])
            # Añadir descripción del objeto
            if not desc_obj_map.empty:
                res['Descripcion Objeto Del Gasto'] = res['Objeto'].map(desc_obj_map)
                res = res[['Objeto', 'Descripcion Objeto Del Gasto', 'FTE', 'Mod. Aprobadas', 'P']]
            else:
                res = res[['Objeto', 'FTE', 'Mod. Aprobadas', 'P']]
            resultados['a.4_Objeto_FTE_POSITIVAS'] = res
            resultados['a.4_Percentiles_Referencia'] = tabla_perc

    # -- a.5 Desagregado por Objeto < 0 --
    if 'Objeto' in df_orig.columns and 'Mod. Aprobadas' in df_orig.columns:
        df_neg = df_orig[df_orig['Mod. Aprobadas'] < 0].copy()
        if not df_neg.empty and 'FTE' in df_neg.columns:
            res = df_neg.groupby(['Objeto', 'FTE']).agg({
                'Mod. Aprobadas': 'sum'
            }).reset_index()
            tabla_perc, res['P'] = calcular_percentiles_serie(res['Mod. Aprobadas'])
            # Añadir descripción del objeto
            if not desc_obj_map.empty:
                res['Descripcion Objeto Del Gasto'] = res['Objeto'].map(desc_obj_map)
                res = res[['Objeto', 'Descripcion Objeto Del Gasto', 'FTE', 'Mod. Aprobadas', 'P']]
            else:
                res = res[['Objeto', 'FTE', 'Mod. Aprobadas', 'P']]
            resultados['a.5_Objeto_FTE_NEGATIVAS'] = res
            resultados['a.5_Percentiles_Referencia'] = tabla_perc

    return resultados

